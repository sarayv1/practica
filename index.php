<li>Es pues que la pizza nace de un alimento elaborado por los habitantes humildes de la ciudad de 
	Nápoles y la composición no fuera tan variada como la actual. Los panes planos y las preparaciones 
	fundamentadas en ellos, como la pizza, son típicos de las cocinas mediterráneas. De esta manera, se tiene la antigua "focaccia" 

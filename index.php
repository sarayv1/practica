	locales ha hecho que la denominación de la pizza haya quedado diluida en una infinidad de variantes regionales.</li><br>

<li>Procedencia de la palabra<br>
Se piensa que la palabra pizza proviene de pinsa, participio pasado del verbo latinopinsere, que significa ‘machacar’, ‘presionar’, ‘aplastar’. Esta descripción etimológica hace referencia a la manera de elaborar la masa de la pizza, así como a su origen en una forma de pan plano, aplastado sobre el piso del horno.

La antigua pizzería Port’Alba (‘puerta blanca’), en la ciudad de Nápoles, es considerada como la primera pizzería del mundo. En 1738 empezaron a producir pizzas para los viandantes 
y en 1830 se expandieron a una especie de pizzería-restaurante con mesas y camareros. Hoy en día sirven pizza bajo las mismas premisas.
Hacia 1830, el escritor francés Alexandre Dumas (padre) describe la pizza en la corte de Nápoles en su novela Le corrícolo. 
Dumas describe la pobreza de la gente que habita la ciudad, a los que denomina lazzaroni (‘lazarones’, como evocación al pobre Lázaro, 
personaje bíblico) y describe cómo esas gentes humildes desayunan, almuerzan y cenan un pan plano al que añaden diversos ingredientes: 
En Nápoles se elaboraba con aceite de oliva, tocino, queso, tomate y anchoas en salazón. El empleo de queso mozzarela (procedente de leche de búfala) 
se introduce en Italia debido a las invasiones de poblaciones procedentes de Asia.

